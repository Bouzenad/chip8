#include "state.h"
#include <stdint.h>
#include <stdio.h>
#include <string.h>

int init_state(system_state_t *state, char *file_name) {
  *state = (system_state_t){0};
  uint8_t digits[NUMBER_OF_DIGITS * BYTE_PER_DIGIT] = {
      0xf0, 0x90, 0x90, 0x90, 0xf0, // 0
      0x20, 0x60, 0x20, 0x20, 0x70, // 1
      0xf0, 0x10, 0xf0, 0x80, 0xf0, // 2
      0xf0, 0x10, 0xf0, 0x10, 0xf0, // 3
      0x90, 0x90, 0xf0, 0x10, 0x10, // 4
      0xf0, 0x80, 0xf0, 0x10, 0xf0, // 5
      0xf0, 0x80, 0xf0, 0x90, 0xf0, // 6
      0xf0, 0x10, 0x20, 0x40, 0x40, // 7
      0xf0, 0x90, 0xf0, 0x90, 0xf0, // 8
      0xf0, 0x90, 0xf0, 0x10, 0xf0, // 9
      0xf0, 0x90, 0xf0, 0x90, 0x90, // A
      0xe0, 0x90, 0xe0, 0x90, 0xe0, // B
      0xf0, 0x80, 0x80, 0x80, 0xf0, // C
      0xe0, 0x90, 0x90, 0x90, 0xe0, // D
      0xf0, 0x80, 0xf0, 0x80, 0xf0, // E
      0xf0, 0x80, 0xf0, 0x80, 0x80  // F
  };
  memcpy(state->memory + DIGIT_LOCATION, digits,
         NUMBER_OF_DIGITS * BYTE_PER_DIGIT);
  state->registers.program_counter = MEMORY_PROGRAM_START;
  FILE *program = fopen(file_name, "rb");
  if (program == NULL) {
    return -1;
  }
  if (fread(state->memory + MEMORY_PROGRAM_START, sizeof(uint8_t),
            MEMORY_END - MEMORY_PROGRAM_START, program) == 0) {
    fclose(program);
    return -2;
  }
  fclose(program);
  return 0;
}
